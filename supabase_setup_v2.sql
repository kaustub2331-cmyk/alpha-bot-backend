create extension if not exists "uuid-ossp";

-- ── bot_status (replaces bot_state) ───────────────────────
-- Single row per user. Updated by the backend worker every 30s.
create table if not exists bot_status (
  id                uuid primary key default uuid_generate_v4(),
  user_id           uuid not null references auth.users(id) on delete cascade,
  status            text not null default 'STOPPED',  -- RUNNING | PAUSED | STOPPED
  server_online     boolean default false,
  last_analysis_time bigint,       -- Unix ms
  last_candle_time   bigint,       -- Unix ms
  error_status      text,
  current_signal    jsonb,         -- { action, direction, strategy, confidence, regime }
  current_regime    text,
  open_positions    integer default 0,
  version           text,
  updated_at        timestamptz not null default now(),
  unique(user_id)
);

