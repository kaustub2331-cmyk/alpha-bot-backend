-- Drop and recreate policies safely
do $$ begin
  -- bot_status
  drop policy if exists "bot_status: own rows" on bot_status;
  create policy "bot_status: own rows" on bot_status
    for all using (auth.uid() = user_id) with check (auth.uid() = user_id);

  -- bot_commands
  drop policy if exists "bot_commands: own rows" on bot_commands;
