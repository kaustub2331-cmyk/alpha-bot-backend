-- ── bot_commands ───────────────────────────────────────────
-- Frontend writes here. Backend reads + acks.
-- Commands: START | STOP | PAUSE | RESUME | UPDATE_SETTINGS
create table if not exists bot_commands (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  command     text not null,       -- START | STOP | PAUSE | RESUME | UPDATE_SETTINGS
  payload     jsonb,               -- optional: { patienceMode, strategyToggles, sizingMode, ... }
  status      text not null default 'PENDING', -- PENDING | DONE
  created_at  timestamptz not null default now(),
  acked_at    timestamptz
);

-- ── engine_logs ────────────────────────────────────────────
-- Backend appends structured log entries. Frontend displays.
create table if not exists engine_logs (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  level       text not null,   -- INFO | TRADE | WARN | ERROR
  message     text not null,
  data        jsonb,
  created_at  timestamptz not null default now()
);

-- ── market_snapshots ───────────────────────────────────────
-- One row per user+symbol. Updated every analysis cycle.
create table if not exists market_snapshots (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  symbol      text not null default 'BTCUSDT',
  data        jsonb,
  updated_at  timestamptz not null default now(),
  unique(user_id, symbol)
);

-- ── signal_log ─────────────────────────────────────────────
-- All signals generated by the backend worker.
create table if not exists signal_log (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  signal_id   text not null,
  data        jsonb,
  updated_at  timestamptz not null default now(),
  unique(signal_id)
);

-- ── Existing tables (from v1 setup — run if not done) ──────

create table if not exists settings (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  key         text not null,
  value       jsonb,
  updated_at  timestamptz not null default now(),
  unique(user_id, key)
);

create table if not exists trades (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  trade_id    text not null,
  data        jsonb,
  updated_at  timestamptz not null default now(),
  unique(trade_id)
);

create table if not exists notes (
  id          uuid primary key default uuid_generate_v4(),
  user_id     uuid not null references auth.users(id) on delete cascade,
  trade_id    text not null,
  note        text,
  updated_at  timestamptz not null default now(),
  unique(user_id, trade_id)
);

